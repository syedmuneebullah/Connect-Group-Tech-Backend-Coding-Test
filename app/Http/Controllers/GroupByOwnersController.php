    {
        //
        $data = $request->json()->all();
    
        $results = [];
    
        foreach ($data as $filename => $company) {
            if (!isset($results[$company])) {
                $results[$company] = [];
            }
            $results[$company][] = $filename;
        }
    
        return response()->json($results);
    }

